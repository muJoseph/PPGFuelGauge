bool mujoeBoardConfig_initBoard( void )
{
    bool retVal = TRUE;
    retVal = muJoeGPIO_initGPIOS();                // Init GPIOs
    mujoeI2C_initHardware( i2cClock_267KHZ );      // Init I2C Hardware
    return retVal;
    
} // muJoeBoardConfig_initBoard

