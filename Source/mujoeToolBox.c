// Returns TRUE if ONLY one bit is set within the input byte
// Returns FALSE otherwise
// NOTE: This needs to be tested to verify if algo is correct
bool mujoeToolBox_oneBitSet_uint8( uint8 byte )
{
  // Catch zero case since algo will return TRUE
  if( byte == 0 )
    return FALSE;
  else
  {
    int8 sbyte = (int8)( (~byte) + 1 ); // Compute negative of input byte
    return ( byte == ( byte & sbyte ) );
  }
  
} // mujoeToolBox_oneBitSet_uint8
