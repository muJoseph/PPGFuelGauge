static mspfg_t  mspfg =
{
  .i2cWriteAddr = MSPFG_DEFAULT_I2C_ADDR,
};

////////////////////////////////////////////////////////////////////////////////
// LOCAL FUNCTION PROTOS
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// API FUNCTIONS
////////////////////////////////////////////////////////////////////////////////

bool mspfg_sendCommand( uint8 cmd )
{
  if( mujoeI2C_write( mspfg.i2cWriteAddr, 1, &cmd, STOP_CMD ) )
    return FALSE;
  else
    return TRUE;
}

